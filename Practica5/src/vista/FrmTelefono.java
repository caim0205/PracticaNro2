    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        try {
            tel.getTelefono().setNombre(cbxMarca.getSelectedItem().toString());
            tel.guardar();

            ep.getEspecificaciones().setModelo(txtModelo.getText());
            ep.getEspecificaciones().setCapacidadA(cbxCapacidad.getSelectedItem().toString());
            ep.getEspecificaciones().setDisplay(cbxDisplay.getSelectedItem().toString());
            ep.getEspecificaciones().setId_telefono(tel.getTelefono().getId());
            ep.guardar();

            tel.setTelefono(null);
            ep.setEspecificaciones(null);
            cargarTabla();

            JOptionPane.showMessageDialog(null, "Telefono guardado ");
        } catch (Exception ex) {
            Logger.getLogger(FrmTelefono.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

