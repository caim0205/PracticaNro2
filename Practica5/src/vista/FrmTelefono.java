    private void buscar() throws Exception {
        String buscar = txtBuscar.getText();
        String metodo = cbxMetodo.getSelectedItem().toString();
        String buscarM = cbxBuscar.getSelectedItem().toString();

        if (buscar.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese una palabra para buscar", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        switch (metodo) {
            case "Binario":
                if (buscarM.equalsIgnoreCase("Modelo")) {
                    modelo.setLista(ep.buscarModeloBin(buscar));
                } else if (buscarM.equalsIgnoreCase("Capacidad")) {
                    modelo.setLista(ep.buscarCapacidadBin(buscar));
                } else if (buscarM.equalsIgnoreCase("Display")) {
                    modelo.setLista(ep.buscarDisplayBin(buscar));
                }
                break;

            case "Lineal Binario":
                if (buscarM.equalsIgnoreCase("Modelo")) {
                    modelo.setLista(ep.buscarModeloLineal(buscar));
                } else if (buscarM.equalsIgnoreCase("Capacidad")) {
                    modelo.setLista(ep.buscarCapacidadALineal(buscar));
                } else if (buscarM.equalsIgnoreCase("Display")) {
                    modelo.setLista(ep.buscarDisplayLineal(buscar));
                }
                break;

            default:
                throw new AssertionError();

        }
        tblTabla.setModel(modelo);
        tblTabla.updateUI();
    }

